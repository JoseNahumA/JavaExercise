package org.example;

public class WhileClass {
    public static void main(String[] args) {
/*
*  While loop
*
        int i = 0;
        while (i < 5){
            System.out.println(" pepito " + i);
            i++;
        }
* */

        // Do while
        int i = 0;
        do {
            System.out.println(i);
            i++;
        } while (i > 5);
    }
}
